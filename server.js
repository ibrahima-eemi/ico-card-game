/**
 * üî• G√©n√®re un code de room al√©atoire.
 */
function generateRoomCode() {
  return crypto.randomBytes(3).toString("hex").toUpperCase();
}

/**
 * üîé Recherche une salle existante.
 */
function findExistingRoom() {
  for (const roomCode in rooms) {
    if (rooms[roomCode].length < 7) {
      return roomCode;
    }
  }
  return null;
}

function assignRoles(roomCode) {
  const room = rooms[roomCode];
  if (!room || room.players.length !== 5) {
    console.error(`‚ùå Impossible d'attribuer les r√¥les : salle ${roomCode} invalide ou incompl√®te.`);
    return;
  }

  const roles = ["Marin", "Marin", "Marin", "Pirate", "Pirate"].sort(() => Math.random() - 0.5);
  console.log(`üé≤ R√¥les g√©n√©r√©s pour la salle ${roomCode} : ${roles.join(", ")}`);

  room.players.forEach((player, index) => {
    const role = roles[index];
    player.role = role;

    if (player.ws.readyState === WebSocket.OPEN) {
      const message = {
        type: "YOUR_ROLE",
        role,
      };
      console.log(`üì§ Envoi du r√¥le √† ${player.username} :`, message);
      player.ws.send(JSON.stringify(message));
    } else {
      console.warn(`‚ö†Ô∏è Connexion WebSocket ferm√©e pour ${player.username}, r√¥le non envoy√©.`);
    }
    console.log(`üé≠ R√¥le attribu√© : ${player.username} ‚Üí ${role}`);
  });
}


/**
 * üì° S√©lectionne un capitaine pour la salle
 */
function assignCaptain(roomCode) {
  const room = rooms[roomCode];
  if (!room || room.players.length === 0) {
    console.error(`‚ùå Pas de joueurs dans la salle ${roomCode}`);
    return;
  }

  room.currentCaptainIndex =
    (room.currentCaptainIndex + 1) % room.players.length;

  const currentCaptain = room.players[room.currentCaptainIndex];
  room.players.forEach((player) => {
    player.isCaptain = player.username === currentCaptain.username;
  });

  console.log(`üëë Nouveau capitaine : ${currentCaptain.username}`);
  broadcast(roomCode, {
    type: "CAPTAIN_SELECTED",
    captain: currentCaptain.username,
    avatar: currentCaptain.avatar,
  });
}


/**
 * üì° Envoie un message √† tous les joueurs d'une salle.
 */
function broadcast(roomCode, message) {
  const room = rooms[roomCode];
  if (!room) {
    console.warn(`‚ö†Ô∏è Aucune salle trouv√©e pour ${roomCode}`);
    return;
  }

  // It√©rer sur les joueurs dans room.players
  room.players.forEach(({ ws }) => {
    if (ws.readyState === WebSocket.OPEN) {
      ws.send(JSON.stringify(message));
    } else {
      console.warn(`‚ö†Ô∏è WebSocket non ouvert pour un client dans la salle ${roomCode}`);
    }
  });
  console.log(`üì§ Message diffus√© √† la salle ${roomCode}:`, message);
}



/**
 * üßπ G√®re la d√©connexion d'un joueur et nettoie les rooms.
 */
function handlePlayerDisconnection(ws) {
  Object.keys(rooms).forEach((roomCode) => {
    const room = rooms[roomCode];

    // Trouve et supprime le joueur correspondant au WebSocket d√©connect√©
    const updatedPlayers = room.players.filter((player) => player.ws !== ws);

    if (updatedPlayers.length === 0) {
      console.log(`üßπ Suppression de la salle vide : ${roomCode}`);
      delete rooms[roomCode];
    } else {
      room.players = updatedPlayers;
      console.log(`üîÑ Mise √† jour des joueurs dans la salle ${roomCode}`);

      // Diffuse la mise √† jour de la salle aux joueurs restants
      const playersList = updatedPlayers.map(({ username, avatar }) => ({ username, avatar }));
      broadcast(roomCode, { type: "ROOM_UPDATE", players: playersList });
    }
  });
}

function revealActions(roomCode) {
  const room = rooms[roomCode];
  if (!room) return;

  // R√©cup√®re les membres de l'√©quipage
  const crew = room.players.filter((p) => p.isCrewMember);

  // Compte le nombre de poison
  const poisonCount = crew.filter((p) => p.currentAction === "poison").length;
  const hasPoison = poisonCount > 0;

  // Incr√©mente les points
  if (hasPoison) {
    // +1 Pirates
    room.players.forEach((pl) => {
      if (pl.role?.toLowerCase() === "pirate") {
        pl.piratePoints = (pl.piratePoints || 0) + 1;
      }
    });
    console.log(`üè¥‚Äç‚ò†Ô∏è [revealActions] Au moins un Poison : Pirates +1`);
  } else {
    // +1 Marins
    room.players.forEach((pl) => {
      if (pl.role?.toLowerCase() === "marin") {
        pl.marinPoints = (pl.marinPoints || 0) + 1;
      }
    });
    console.log(`‚öì [revealActions] 3 √éles : Marins +1`);
  }

  // Pr√©pare la liste des actions
  const actions = crew.map((p) => ({
    username: p.username,
    action: p.currentAction,
  }));

  const winningSide = hasPoison ? "pirates" : "marins";

  broadcast(roomCode, {
    type: "ACTIONS_REVEALED",
    actions,
    winningSide,
  });

  console.log(`üì§ [revealActions] ACTIONS_REVEALED :`, { actions, winningSide });

  // Reset
  room.players.forEach((p) => (p.currentAction = null));
  room.crewActionsCount = 0;
  console.log(`‚ôªÔ∏è [revealActions] Remise √† z√©ro de currentAction et crewActionsCount`);
}
